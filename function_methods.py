# methods = functions of datatypes.
# .upper for getting an argument in all caps 
# .lower for everything in lowercase.
# .replace to replace anything (put that in single quotes)

print('value'.upper())
print("VALUE".lower())
print('value'.replace('e' , '2'))

# abs for tuning any negative number into positive number
print(abs(-4))

# max for getting the maximum value 
# min for getting the minimum value
print(max(10, 1000, 30, 45))
print(min(10, 1000, 30, 45))

# len for word count
print(len('Saloni Sharma'.replace(' ' , '')))






